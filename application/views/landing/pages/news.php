                                    <p>
                                    Liputan6.com, Jakarta FWD Unstoppable Music merupakan bagian dari kampanye FWD Life Unstoppable Passion yang bertujuan untuk memperkenalkan asuransi kepada masyarakat luas melalui pendekatan passion.

"Melalui pendekatan passion, FWD Life ingin mengubah cara pandang masyarakat tentang berasuransi. Bahwa asuransi tidak menakutkan. Melihat asuransi dari sudut pandang yang berbeda sesuai passion masing-masing, salah satunya musik," ujar Maika Randini, Chief Marketing Officer FWD Life saat membuka acara FWD Unstoppable Music yang dimeriahkan oleh Sheila On 7.
                                    </p>

                                    <p>
                                    Perayaan Anniversary atau ulang tahun memang pas sekali diisi dengan hiburan musik. Orang-orang bisa bernyanyi, berjoget dan bahagia bersama sambil menyalurkan passionnya. Merayakan hari jadi yang ke-7, FWD Life Indonesia kembali menghadirkan Unstoppable Music dengan bintang utama Sheila On 7.

Hampir satu jam lamanya, Sheila On 7 menghibur para Passionate People. Total ada 10 lagu-lagu hits dan nostalgia dibawakan Duta cs. Mulai dari 'Bila Kau Tak Disampingku', 'Seberapa Pantas', 'Kita'. Tanpa komando para passionate people pun kompak bernyanyi.  
                                    </p>
                                    <p>"Selamat datang di Lucy in The Sky, Selamat datang buat teman-teman passionate people. Tepuk tangan untuk FWD Life. Asuransi Hidup," kata Duta yang kemudian disambut tepuk tangan para penonton.

Setelah mengajak passionate people bernyanyi lagu-lagu tahun 99 dan 2000 an, Sheila On 7 lalu mengajak penonton ke tahun 2011 untuk bernyanyi 'Hari Bersamanya' Dilanjutkan dengan lagu-lagu yang tergolong baru 'Film Favorit' dan 'Lapang Dada'.</p>

                                  

